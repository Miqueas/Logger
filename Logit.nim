import std/times
from std/os import getTempDir
from std/strutils import join

type
  LogKind* = enum
    OTHER,
    TRACE,
    DEBUG,
    INFO,
    WARN,
    ERROR,
    FATAL
  Logit* = object
    path: string
    namespace: string
    fileSuffix: string
    enableFile: bool
    defaultLevel: LogKind
    enableConsole: bool

const FMT = (
  FileName: "%s_%s.log",
  Out: (
    File: "%s [%s %s] %s@%s: %s",
    Console: e(2) & "%s [" & e(0,1) & "%s %s%s" & e(0,2) & "] %s@%s:" & e(0) & " %s"
  )
)

proc e(n: varargs[int]): string =
  return '\e' & '[' & join(n, ";") & 'm'

proc newLogit*(path = getTempDir(), name = "Logit", level = OTHER, console = false, file = true, suffix = ""): Logit =
  var o = Logit()
  o.path = path
  o.namespace = name
  o.fileSuffix = suffix
  o.enableFile = file
  o.defaultLevel = level
  o.enableConsole = console

  let
    header = "AUTOGENERATED BY LOGIT"
    time = now()
    #file = open()

  return o